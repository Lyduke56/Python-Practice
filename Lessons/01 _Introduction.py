# Checking data types
print(type(10))                         # int data type, decimal numbers
print(type(3.14))                       # float data type, floating point numbers
print(type(1 + 3j))                     # complex data type, imaginary number/equation
print(type('Lyduke Test Test'))         # string data type, characters
print(type({'name': 'Lyduke'}))         # dictionary data type, key value pairs as in 'key' : 'value'
print(type([1, 2, 3, 6, 7]))            # list data type, ordered, changeable, duplicates
print(type({2, 3 ,6}))                  # set data type, unordered, changeable, no duplicates
print(type((1, 1, 3, 5, 2)))            # tuple data type, ordered, unchangeable, duplicates

